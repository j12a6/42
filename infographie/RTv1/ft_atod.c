double		ft_atod(const char *str)
{
	double		i;
	double		j;

	i = 1;
	j = 0;
	while (*str == ' ' || *str == '\n' || *str == '\v' || *str == '\t'
	|| *str == '\f' || *str == '\r')
		str++;
	if ((str[0] == '+' && str[1] == '-') || (str[0] == '-' && str[1] == '+'))
		return (0);
	if (*str == '-')
	{
		i = -1;
		str++;
	}
	if (*str == '+')
		str++;
	while (*str <= '9' && *str >= '0')
	{
		j = *str - '0' + j * 10;
		str++;
	}
	return (j * i);
}
