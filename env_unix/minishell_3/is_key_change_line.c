int			ft_is_key_opt_arrow_up(char *buf)
{
	if ((buf[0] == 27 && buf[1] == 27 && buf[2] == 91 && buf[3] == 65
		&& buf[4] == 0 && buf[5] == 0)
		|| (buf[0] == 27 && buf[1] == 91 && buf[2] == 49 && buf[3] == 59
			&& buf[4] == 50 && buf[5] == 65))
		return (1);
	return (0);
}

int			ft_is_key_opt_arrow_down(char *buf)
{
	if ((buf[0] == 27 && buf[1] == 27 && buf[2] == 91 && buf[3] == 66
		&& buf[4] == 0 && buf[5] == 0)
		|| (buf[0] == 27 && buf[1] == 91 && buf[2] == 49 && buf[3] == 59
			&& buf[4] == 50 && buf[5] == 66))
		return (1);
	return (0);
}
