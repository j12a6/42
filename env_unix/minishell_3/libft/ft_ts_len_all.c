#include "libft.h"

int		ft_ts_len_all(char **ts)
{
	int		i;
	int		len;

	len = 0;
	i = 0;
	while (ts[i])
	{
		len	+= (int)ft_strlen(ts[i]);
		++i;
	}
	return (len);
}
