#include "libft.h"

void	ft_ts_print(char **ts)
{
	int		i;

	if (!ts)
	{
		ft_putendl("(null)");
		return ;
	}
	i = 0;
	while (ts[i])
	{
		ft_putendl(ts[i]);
		++i;
	}
}
